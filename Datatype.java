class Datatype
{
	public static void main(String args[])
	{
		// identifier matlab jo hum hamare taraf se likhte hai(classname,variablename)
		byte sanika=50;
		System.out.println("value of byte is: "+sanika);
		System.out.println("**********"+sanika+"************");
		System.out.println(sanika+" is stored in a variable");
		
		short a=20000;
		System.out.println("value of a is: "+a);
		
		int b=27762665;
		System.out.println("value of b is: "+b);
		
		float c=12.96f;
		System.out.println("value of c is: "+c);
		
		long d=988874178783l;
		System.out.println(d);
		
		double e=123.67556723;
		System.out.println(e);
		
		char f='$';
		System.out.println(f);
		
		boolean g=true;   //false
		System.out.println(g);
	}
	
}