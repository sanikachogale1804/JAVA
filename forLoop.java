class forLoop
{
	public static void main (String args[])
	{
		for(int i=0;i<=5;i++)
		{
			System.out.println(i);
		}
		for(int i=5;i>=0;i--)
		{
			System.out.println(i);
		}
		for(int i=13;i<=20;i++)
		{
			System.out.print(i+" ");
		}
		System.out.println();
		for(int i=21;i>=13;i--)
		{
			System.out.print(i+" ");
		}
	}
}
